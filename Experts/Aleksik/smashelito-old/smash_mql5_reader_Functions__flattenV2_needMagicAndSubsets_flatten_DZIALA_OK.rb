OUTPUT_FILE_SUBSETS = "smash_mql5_reader_Functions__flattenV2_needMagicAndSubsets_flattened_subsets.txt"
OUTPUT_FILE_MAGICS  = "smash_mql5_reader_Functions__flattenV2_needMagicAndSubsets_flattened_magics.txt"

# ================== CONFIG ==================

first_digit = "2"
second_digits_allowed = [0, 1, 2, 3, 4]
third_digit = "3"

starting_index_digits4th5th = "01"
SLOTS_PER_SECOND_DIGIT = 99

# ================== STEP 1 ==================

input_magics = magic_input.lines.map(&:strip).reject(&:empty?)

# GROUP BY SUBSET PREFIX (THIS IS THE FIX)
groups = input_magics.group_by { |m| m[0..4] }

pull_subsets = groups.keys

# ================== STEP 2 ==================

content = File.read(INPUT_FILE)

# ================== STEP 3 ==================

blocks = {}

regex = /bool\s+Subset_(\d{5}.*?)\{.*?\n\}/m

content.scan(regex) do |match|
  full_block = $&
  subset_id = match[0][0..4]

  if pull_subsets.include?(subset_id)
    blocks[subset_id] = full_block
  end
end

raise "No matching subset blocks found!" if blocks.empty?

# ================== STEP 4: FLATTEN ==================

start_suffix = starting_index_digits4th5th.to_i
current_slot = start_suffix - 1

flattened_magics = []
unique_subsets = {}

groups.each_with_index do |(subset_prefix, magics), group_index|

  slot = current_slot + group_index

  digit_idx = slot / SLOTS_PER_SECOND_DIGIT
  suffix_idx = slot % SLOTS_PER_SECOND_DIGIT

  raise "Exceeded allowed second digits!" if digit_idx >= second_digits_allowed.size

  second_digit = second_digits_allowed[digit_idx]
  suffix = format("%02d", suffix_idx + 1)

  # NEW SUBSET ID (ONE PER GROUP)
  new_subset = "#{first_digit}#{second_digit}#{third_digit}#{suffix}"

  # only build subset once
  unless unique_subsets.key?(new_subset)
    block = blocks[subset_prefix]
    raise "Missing block for subset #{subset_prefix}" unless block

    new_block = block.sub(/Subset_\d{5}/, "Subset_#{new_subset}")
    unique_subsets[new_subset] = new_block
  end

  # now process ALL magics in group
  magics.each do |magic|
    new_magic = magic.dup

    new_magic[0] = first_digit
    new_magic[1] = second_digit.to_s
    new_magic[2] = third_digit
    new_magic[3..4] = suffix

    flattened_magics << new_magic
  end
end

# ================== STEP 5 ==================

File.write(OUTPUT_FILE_SUBSETS, unique_subsets.values.join("\n\n"))
File.write(OUTPUT_FILE_MAGICS, flattened_magics.join("\n"))

puts "Done."
puts "Unique subsets: #{unique_subsets.size}"
puts "Magics: #{flattened_magics.size}"