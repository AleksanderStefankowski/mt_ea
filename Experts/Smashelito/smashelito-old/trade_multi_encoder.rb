final_magics.each_with_index do |magic, i|
  data = parse_magic(magic)
  babysit_enabled, babysit_minute = parse_babysit(data[:babysit_raw])
  idx = TRADE_INDEX_START + i

  block = []
  block << "\n// encoding input magic: #{magic}"
